	SUBROUTINE WRITE_CMAT(FL,FREQ_INDX,DIAG_INDX,DESC)
	USE MOD_CMFGEN
	USE STEQ_DATA_MOD
	IMPLICIT NONE
!
	REAL*8 FL
	INTEGER DIAG_INDX,FREQ_INDX
	INTEGER J,ID
	CHARACTER(LEN=*) DESC
!
	IF(FL .GT. 22.0D0 .OR. FL .LE. 20.0D0)RETURN
!
	ID=16
	DO J=ATM(ID)%NXzV+1,SE(ID)%N_SE-1
	  WRITE(316,'(4ES16.4,3X,A,I6,ES18.10)')
	1      SE(ID)%BA_PAR(1,J,6),SE(ID)%BA_PAR(1,J,7),
	1      SE(ID)%BA(1,J,DIAG_INDX,6),SE(ID)%BA(1,J,DIAG_INDX,7),TRIM(DESC),FREQ_INDX,FL
	END DO
!
	ID=17
	DO J=ATM(ID)%NXzV+1,SE(ID)%N_SE-1
	  WRITE(317,'(4ES16.4,3X,A,I6,ES18.10)')
	1      SE(ID)%BA_PAR(1,J,6),SE(ID)%BA_PAR(1,J,7),
	1      SE(ID)%BA(1,J,DIAG_INDX,6),SE(ID)%BA(1,J,DIAG_INDX,7),TRIM(DESC),FREQ_INDX,FL
	END DO
!
	ID=18
	DO J=ATM(ID)%NXzV+1,SE(ID)%N_SE-1
	  WRITE(318,'(4ES16.4,3X,A,I6,ES18.10)')
	1      SE(ID)%BA_PAR(1,J,6),SE(ID)%BA_PAR(1,J,7),
	1      SE(ID)%BA(1,J,DIAG_INDX,6),SE(ID)%BA(1,J,DIAG_INDX,7),TRIM(DESC),FREQ_INDX,FL
	END DO
!
	ID=19
	DO J=ATM(ID)%NXzV+1,SE(ID)%N_SE-1
	  WRITE(319,'(4ES16.4,3X,A,I6,ES18.10)')
	1      SE(ID)%BA_PAR(1,J,6),SE(ID)%BA_PAR(1,J,7),
	1      SE(ID)%BA(1,J,DIAG_INDX,6),SE(ID)%BA(1,J,DIAG_INDX,7),TRIM(DESC),FREQ_INDX,FL
	END DO
	FLUSH(UNIT=316)
	FLUSH(UNIT=317)
	FLUSH(UNIT=318)
	FLUSH(UNIT=319)
!
	RETURN
	END
