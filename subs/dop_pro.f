C
C Function to compute a gaussian line profile.
C
	FUNCTION DOP_PRO(PF,FL,T,VTURB,AMASS)
	IMPLICIT NONE
C
C Altered 24-May-1996 : DEXP removed
C
	REAL*8 PF,FL,T,VTURB,AMASS,DOP_PRO
C
C Local variables
C
	REAL*8 SCL,RDELD
	PARAMETER (SCL=1.0D-15/1.77245385095516D0)    !1.0D-15/SQRT(PI)
C
	RDELD=2.333015D+04/SQRT( T/AMASS + (VTURB/12.85D0)**2 )
	DOP_PRO=SCL*RDELD*EXP(-(PF*RDELD)**2)/FL
C
	RETURN
	END
